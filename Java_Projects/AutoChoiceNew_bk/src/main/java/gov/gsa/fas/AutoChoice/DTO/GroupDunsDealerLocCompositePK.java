public class GroupDunsDealerLocCompositePK implements Serializable{

	protected int group_DUNS = 0;
	protected String dealer_Code = "";
	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result
				+ ((dealer_Code == null) ? 0 : dealer_Code.hashCode());
		result = prime * result + group_DUNS;
		return result;
	}
	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		GroupDunsDealerLocCompositePK other = (GroupDunsDealerLocCompositePK) obj;
		if (dealer_Code == null) {
			if (other.dealer_Code != null)
				return false;
		} else if (!dealer_Code.equals(other.dealer_Code))
			return false;
		if (group_DUNS != other.group_DUNS)
			return false;
		return true;
	}
	
	

}

