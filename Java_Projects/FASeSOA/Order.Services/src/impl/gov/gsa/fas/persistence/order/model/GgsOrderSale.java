package gov.gsa.fas.persistence.order.model;

import java.io.Serializable;

/**
 * <code>GgsOrderSale</code> contains GGS (Web Order) Order sale information.
 *
 * @author  PrasadRNerayanuru
 * @version 1.0
 */

public class GgsOrderSale implements Serializable {

	private static final long serialVersionUID = -6558452374548311404L;
	
	private Long orderNumber;
	
	private String store;
	
	private Long saleWebOrdDate;
	
	private Long webOrderNumber;
	
	private String AAC;
	
	private String access;
	
	private String liidId;


	public Long getOrderNumber() {
		return orderNumber;
	}

	public void setOrderNumber(Long orderNumber) {
		this.orderNumber = orderNumber;
	}

	public String getStore() {
		return store;
	}

	public void setStore(String store) {
		this.store = store;
	}

	public Long getSaleWebOrdDate() {
		return saleWebOrdDate;
	}

	public void setSaleWebOrdDate(Long saleDate) {
		this.saleWebOrdDate = saleDate;
	}

	public Long getWebOrderNumber() {
		return webOrderNumber;
	}

	public void setWebOrderNumber(Long webOrderNumber) {
		this.webOrderNumber = webOrderNumber;
	}

	public String getAAC() {
		return AAC;
	}

	public void setAAC(String aAC) {
		AAC = aAC;
	}

	public String getAccess() {
		return access;
	}

	public void setAccess(String access) {
		this.access = access;
	}

	/**
	 * @return the liidId
	 */
	public String getLiidId() {
		return liidId;
	}

	/**
	 * @param liidId the liidId to set
	 */
	public void setLiidId(String liidId) {
		this.liidId = liidId;
	}

}
