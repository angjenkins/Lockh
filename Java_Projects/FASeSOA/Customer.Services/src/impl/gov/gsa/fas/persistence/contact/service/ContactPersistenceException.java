package gov.gsa.fas.persistence.contact.service;

/**
 * <code>ContactPersistenceException</code> is the custom exception
 * while accessing Customer Contact details.
 *
 * @author  PrasadRNerayanuru
 * @version 1.0
 */

public class ContactPersistenceException extends Exception {

	private static final long serialVersionUID = 2845474117341887225L;

	public ContactPersistenceException(String message, Throwable cause) {
		super(message, cause);
	}

	public ContactPersistenceException(String message) {
		super(message);
	}

	public ContactPersistenceException(Throwable cause) {
		super(cause);
	}

}
